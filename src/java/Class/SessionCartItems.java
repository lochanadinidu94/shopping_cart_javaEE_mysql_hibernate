package Class;

/**
 *
 * @author Lochana
 */
public class SessionCartItems {
    
    private int Pid;
    private String Color;
    private int Qty;

    /**
     * @return the Pid
     */
    public int getPid() {
        return Pid;
    }

    /**
     * @param Pid the Pid to set
     */
    public void setPid(int Pid) {
        this.Pid = Pid;
    }

    /**
     * @return the Color
     */
    public String getColor() {
        return Color;
    }

    /**
     * @param Color the Color to set
     */
    public void setColor(String Color) {
        this.Color = Color;
    }

    /**
     * @return the Qty
     */
    public int getQty() {
        return Qty;
    }

    /**
     * @param Qty the Qty to set
     */
    public void setQty(int Qty) {
        this.Qty = Qty;
    }
    
}
